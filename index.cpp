void permute(vector<int>& a, int l, int r)
{
    if (l == r)
        {printVector(a);}
    else {
        // Permutations made
        for (int i = l; i <= r; i++) {
 
            // Swapping done
            swap(a[l], a[i]);
 
            // Recursion called
            permute(a, l + 1, r);
 
            // backtrack
            swap(a[l], a[i]);
        }
    }
}

vector<int> InterviewBit(vector<int> &a) {
    int n = a.size() , fg = 0;
    for(int i=n-1; i>0; i--) {
        if(a[i]>a[i-1]) {
            swap(a[i],a[i-1]);
            fg = 1;
            break;
        }
    }
    if(!fg) reverse(all(a));
    return a;
}


